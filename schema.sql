-- new table
CREATE TABLE vets (
  id INT GENERATED BY DEFAULT AS IDENTITY,
  name VARCHAR(100),
  age INTEGER,
  date_of_graduation DATE,
  PRIMARY KEY (id)
);

CREATE TABLE specializations (
  vet_id INTEGER,
  species_id INTEGER,
  PRIMARY KEY (vet_id, species_id),
  FOREIGN KEY (vet_id) REFERENCES vets(id)
  ON DELETE CASCADE,
  FOREIGN KEY (species_id) REFERENCES species(id)
  ON DELETE CASCADE
);

CREATE TABLE visits (
  id INT GENERATED BY DEFAULT AS IDENTITY,
  animal_id INTEGER,
  vet_id INTEGER,
  date_of_visit DATE,
  PRIMARY KEY (id),
  FOREIGN KEY (animal_id) REFERENCES animals(id)
  ON DELETE CASCADE,
  FOREIGN KEY (vet_id) REFERENCES vets(id)
  ON DELETE CASCADE
);